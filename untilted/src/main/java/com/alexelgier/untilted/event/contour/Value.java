package com.alexelgier.untilted.event.contour;

public class Value {

  private double xvalue;
  private double yvalue;

  public Value(double xvalue, double yvalue) {
    this.xvalue = xvalue;
    this.yvalue = yvalue;
  }

  public double getXValue() {
    return xvalue;
  }

  public double getYValue() {
    return yvalue;
  }

}
